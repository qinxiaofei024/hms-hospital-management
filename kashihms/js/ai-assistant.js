    addStreamingMessage: function(container, sender, fullText, options = {}) {
        const messageDiv = document.createElement('div');
        messageDiv.className = `message ${sender}`;
        
        const avatarDiv = document.createElement('div');
        avatarDiv.className = 'message-avatar';
        
        const contentDiv = document.createElement('div');
        contentDiv.className = 'message-content';
        
        // å…ˆæ˜¾ç¤ºæ‰“å­—ä¸­æŒ‡ç¤ºå™¨
        const typing = document.createElement('span');
        typing.className = 'typing';
        typing.innerHTML = '<span class="dot"></span><span class="dot"></span><span class="dot"></span>';
        contentDiv.appendChild(typing);
        
        messageDiv.appendChild(avatarDiv);
        messageDiv.appendChild(contentDiv);
        container.appendChild(messageDiv);
        
        // ç¡®ä¿å¯è§
        container.scrollTop = container.scrollHeight;
        
        // ç¨ä½œå»¶è¿Ÿåå¼€å§‹é€å­—è¾“å‡º
        setTimeout(() => {
            contentDiv.textContent = '';
            const text = fullText || '';
            const total = text.length;
            // æ§åˆ¶æ­¥é•¿ï¼Œæ–‡æœ¬è¶Šé•¿å•æ­¥è¶Šå¤§ï¼Œä¿è¯æ•´ä½“é€Ÿåº¦ï¼›åŒæ—¶ä¿æŒâ€œé€æ¸è¾“å‡ºâ€çš„è§‚æ„Ÿ
            const step = Math.max(1, Math.ceil(total / 400));
            let i = 0;
            const timer = setInterval(() => {
                // æ¯æ¬¡è¿½åŠ  step ä¸ªå­—ç¬¦
                const next = text.slice(i, Math.min(i + step, total));
                contentDiv.textContent += next;
                i += step;
                
                // è¾¹æ‰“å­—è¾¹æ»šåŠ¨åˆ°åº•éƒ¨
                container.scrollTop = container.scrollHeight;
                
                if (i >= total) {
                    clearInterval(timer);
                    // å®Œæˆå›è°ƒï¼ˆç”¨äºæ¢å¤å‘é€æŒ‰é’®ç­‰ï¼‰
                    if (options.onComplete) options.onComplete();
                }
            }, 16); // çº¦ ~60fps çš„é€å¸§æ›´æ–°
        }, 200);
    },
    
